"""
+ (Adição)

- (Subtração)

* (Multiplicação)

/ (Divisão)

** (Exponenciação)

% (Resto de uma divisão)

// (Parte inteira do Quociente da divisão)

math.sqrt ou valor ** 0.5 (Raiz Quadrada)
"""

adicao = 2 + 3

subtracao = 2 - 3

multiplicacao = 2 * 3

divisao = 2 / 3

exponenciacao = 2 ** 3

resto_divisao = 2 % 3

quociente_inteiro = 2 // 3

raiz_quadrada = 144 ** 0.5

print(adicao)
print(subtracao)
print(multiplicacao)
print(divisao)
print(exponenciacao)
print(resto_divisao)
print(quociente_inteiro)
print(raiz_quadrada)
